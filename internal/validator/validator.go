package validator

import "regexp"

var (
	EmailRX = regexp.MustCompile("^[a-zA-Z0-9.!#$%&'*+\\/=?^_`{|}~-]+@[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?(?:\\.[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?)*$")
)

type Validator struct {
	Errors map[string]string
}

// New is a helper which creates a new Validator instance with an empty errors map.
func New() *Validator {
	return &Validator{Errors: make(map[string]string)}
}

// Valid returns true if the errors map doesn't contain any entries.
func (v *Validator) Valid() bool {
	return len(v.Errors) == 0
}

// AddError adds an error message to the map (so long as no entry already exists for
// the given key).
func (v *Validator) AddError(key, message string) {
	if _, exists := v.Errors[key]; !exists {
		v.Errors[key] = message
	}
}

// Check adds an error message to the map only if a validation check is not 'ok'.
func (v *Validator) Check(ok bool, key, message string) {
	if !ok {
		v.AddError(key, message)
	}
}

// Generic function which returns true if a specific value is in a list.
func IsPermitted[T comparable](value T, permittedValues ...T) bool {
	for i := range permittedValues {
		if value == permittedValues[i] {
			return true
		}
	}
	return false
}

// IsMatch returns true if a string value matches a specific regexp pattern.
func IsMatch(value string, rx *regexp.Regexp) bool {
	return rx.MatchString(value)
}

// IsValidEmail returns true if a string value is a valid email address.
func IsValidEmail(email string) bool {
	return EmailRX.MatchString(email)
}

// Generic function which returns true if all values in a slice are unique.
func IsUnique[T comparable](values []T) bool {
	c := make(map[T]bool)
	for _, value := range values {
		if _, exists := c[value]; exists {
			return false
		}
		c[value] = true
	}
	return true
}
